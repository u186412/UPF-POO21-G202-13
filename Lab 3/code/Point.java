
public class Point {
    //Atributes//
    protected double x; 
    protected double y;
    //Methods//
    public Point(double xi, double yi) {
        this.x = xi;
        this.y = yi;
    }
    public double getX() {
        return this.x;
    }
    public double getY() {
        return this.y;
    }
}
